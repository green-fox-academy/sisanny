// Create a program that writes this line 100 times:
// "I won't cheat on the exam!"

public class IWontCheatOnTheExam {
    public static void main(String[] args) {
        int count = 0;
        while (count < 100) {
            System.out.println("I won't cheat on the exam!");
            count ++;
        }
    }
}
